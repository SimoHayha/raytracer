#include "lib.h"

void	m4_mult_matcol(double m1[16], double m2[4])
{
	double	m3[4];

	m3[0] = m1[0] * m2[0] + m1[1] * m2[1] + m1[2] * m2[2] + m1[3] * m2[3];
	m3[1] = m1[4] * m2[0] + m1[5] * m2[1] + m1[6] * m2[2] + m1[7] * m2[3];
	m3[2] = m1[8] * m2[0] + m1[9] * m2[1] + m1[10] * m2[2] + m1[11] * m2[3];
	m3[3] = m1[12] * m2[0] + m1[13] * m2[1] + m1[14] * m2[2] + m1[15] * m2[3];
	m2[0] = m3[0];
	m2[1] = m3[1];
	m2[2] = m3[2];
	m2[3] = m3[3];
}
